-- Spark Mall Supabase schema aligned with current frontend API usage
-- Compatible with Supabase PostgreSQL

create extension if not exists pgcrypto;

-- =========================
-- Core user tables
-- =========================
create table if not exists public.users (
  id bigint generated by default as identity primary key,
  phone text unique,
  password text,
  username text,
  nickname text,
  avatar text,
  balance numeric(12,2) not null default 0,
  points integer not null default 0,
  sex text,
  is_agent integer not null default 0,
  open_id text,
  token text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.users is '用户表';
comment on column public.users.phone is '手机号';
comment on column public.users.password is '登录密码';
comment on column public.users.username is '登录名/账号';
comment on column public.users.nickname is '昵称';
comment on column public.users.avatar is '头像';
comment on column public.users.balance is '余额';
comment on column public.users.points is '积分';
comment on column public.users.sex is '性别';
comment on column public.users.is_agent is '是否代理 1是 0否';
comment on column public.users.open_id is '微信 openid';
comment on column public.users.token is '登录 token';

create table if not exists public.product_categories (
  id bigint generated by default as identity primary key,
  cate_name text not null,
  icon text,
  sort integer not null default 0,
  status integer not null default 1,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.product_categories is '商品分类表';

create table if not exists public.products (
  id bigint generated by default as identity primary key,
  cate_id bigint references public.product_categories(id) on delete set null,
  product_name text not null,
  product_grade text,
  premium numeric(12,2) not null default 0,
  exchange_price numeric(12,2) not null default 0,
  dismantle_price numeric(12,2) not null default 0,
  shipping_fee numeric(12,2) not null default 0,
  store_page_icon text,
  product_detail_ad_image text,
  product_detail_images jsonb not null default '[]'::jsonb,
  show_store integer not null default 1,
  is_recommend boolean not null default false,
  is_hot boolean not null default false,
  status integer not null default 1,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.products is '商品表';

create table if not exists public.product_boxes (
  id bigint generated by default as identity primary key,
  box_id bigint unique,
  name text not null,
  description text,
  img_src text,
  bg_type integer not null default 1,
  icon_type integer not null default 1,
  price numeric(12,2) not null default 0,
  first_price numeric(12,2) not null default 0,
  discount_price numeric(12,2) not null default 0,
  prize_price_limit text,
  type integer not null default 1,
  detail_img text,
  sort integer not null default 0,
  status integer not null default 1,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.product_boxes is '盲盒表';

create table if not exists public.product_box_items (
  id bigint generated by default as identity primary key,
  box_id bigint not null references public.product_boxes(id) on delete cascade,
  product_id bigint not null references public.products(id) on delete cascade,
  draw_weight integer not null default 1,
  sort integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.product_box_items is '盲盒-商品关联表';

create table if not exists public.banners (
  id bigint generated by default as identity primary key,
  type integer not null default 1,
  box_id bigint references public.product_boxes(id) on delete set null,
  img_src text not null,
  detail_img text,
  link_url text,
  sort integer not null default 0,
  status integer not null default 1,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.banners is '首页轮播图/公告';

create table if not exists public.addresses (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  contact_name text not null,
  contact_phone text not null,
  province text not null,
  city text not null,
  district text not null,
  detail_address text not null,
  area_values text,
  is_default boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.addresses is '收货地址表';

create table if not exists public.orders (
  id bigint generated by default as identity primary key,
  order_no text not null unique,
  user_id bigint not null references public.users(id) on delete cascade,
  box_id bigint references public.product_boxes(id) on delete set null,
  order_type integer,
  status integer not null default 0,
  pay_status integer not null default 0,
  total_price numeric(12,2) not null default 0,
  shipping_fee numeric(12,2) not null default 0,
  total_points numeric(12,2) not null default 0,
  exchange_total_points numeric(12,2) not null default 0,
  product_count integer not null default 0,
  product_list jsonb not null default '[]'::jsonb,
  exchange_list jsonb not null default '[]'::jsonb,
  address jsonb,
  content text,
  detail_img text,
  expire_time timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.orders is '订单表';

create table if not exists public.user_box_products (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  product_id bigint not null references public.products(id) on delete cascade,
  source_order_no text,
  status integer not null default 0,
  price numeric(12,2) not null default 0,
  sell_price numeric(12,2) not null default 0,
  product_snapshot jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.user_box_products is '用户盒柜商品表';

create table if not exists public.consignment_orders (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  user_box_product_id bigint references public.user_box_products(id) on delete set null,
  status integer not null default 0,
  sell_price numeric(12,2) not null default 0,
  product_snapshot jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.consignment_orders is '寄售订单表';

create table if not exists public.messages (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  title text not null,
  content text not null,
  detail_img text,
  is_read boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.messages is '站内消息表';

create table if not exists public.withdrawal_apply_list (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  amount numeric(12,2) not null default 0,
  bank_name text,
  account_name text,
  account_no text,
  status smallint not null default 0,
  remark text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.withdrawal_apply_list is '提现申请表';

create table if not exists public.user_invite_stats (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  stat_date date not null,
  total_invit_count integer not null default 0,
  total_commission numeric(12,2) not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.user_invite_stats is '邀请统计表';

create table if not exists public.user_invites (
  id bigint generated by default as identity primary key,
  inviter_user_id bigint not null references public.users(id) on delete cascade,
  invited_user_id bigint not null references public.users(id) on delete cascade,
  invited_nickname text,
  invited_avatar text,
  invited_phone text,
  level_no smallint not null default 1,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.user_invites is '邀请列表表';

create table if not exists public.user_commissions (
  id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(id) on delete cascade,
  source_user_id bigint references public.users(id) on delete set null,
  source_order_no text,
  commission_type text not null default 'invite',
  amount numeric(12,2) not null default 0,
  status smallint not null default 1,
  note text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.user_commissions is '佣金明细表';

create table if not exists public.companies (
  id bigint generated by default as identity primary key,
  name text not null,
  logo text,
  intro text,
  address text,
  phone text,
  sort_order integer not null default 0,
  status smallint not null default 1,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);
comment on table public.companies is '商户表';

-- =========================
-- Indexes
-- =========================
create index if not exists idx_product_categories_sort on public.product_categories(sort);
create index if not exists idx_products_cate_id on public.products(cate_id);
create index if not exists idx_products_status on public.products(status);
create index if not exists idx_products_show_store on public.products(show_store);
create index if not exists idx_product_boxes_sort on public.product_boxes(sort);
create index if not exists idx_product_box_items_box_id on public.product_box_items(box_id);
create index if not exists idx_orders_user_id on public.orders(user_id);
create index if not exists idx_orders_status on public.orders(status);
create index if not exists idx_addresses_user_id on public.addresses(user_id);
create index if not exists idx_messages_user_id on public.messages(user_id);
create index if not exists idx_user_box_products_user_id on public.user_box_products(user_id);
create index if not exists idx_user_box_products_status on public.user_box_products(status);
create index if not exists idx_consignment_orders_user_id on public.consignment_orders(user_id);
create index if not exists idx_consignment_orders_status on public.consignment_orders(status);
create index if not exists idx_withdrawal_apply_user_id on public.withdrawal_apply_list(user_id);
create index if not exists idx_user_invites_inviter_user_id on public.user_invites(inviter_user_id);
create index if not exists idx_user_commissions_user_id on public.user_commissions(user_id);
create index if not exists idx_companies_sort_order on public.companies(sort_order);

